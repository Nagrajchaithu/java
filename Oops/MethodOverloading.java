package Oops;

public class MethodOverloading {
	
  public void hari() {
	  int a=10;
	  int b=20;
	  System.out.println(a+b);
	  
  }

	
  public void hari(int x,int y) {
	
	  System.out.println(x+y);
	  
	  
  }
  public void hari(String a, String b) {
	  
	  System.out.println(a);
	  System.out.println(b);
  }

}
